    <h1>Kesaron 100% Buyer Protection Program Details:</h1>
  </div>
  <p>Kesaron.com has a friendly return and refund policy to ensure   your online purchase is free of stress. We offer "100% Buyer Protection   Program" for our valued customers. We do not release your payment to the   seller till the products are received by you and you are happy with it.   We are always with you, before your purchase and after your purchase.   We are not perfect but we have ensured hard that our refund/return   policies do not bring any ugly surprises to you post your purchase.</p>
  <br>
  <br>
  <div><strong>What is Kesaron&rsquo;s Return Policy?</strong>
    <p>we dont appriciate or support refund or exchange articles however Our "100% Buyer Protection Program" allows for easy returns and refunds for:</p>
    <br>
    <ul>
      <li>1. Products which you received in "Damaged/Defective/Broken" condition.</li>
      <li>2. Products not as per description/photo on the website.</li>
      <li>3. Products which you may not like for quality, color and design related issues.</li>
      <br>
    </ul>
    <p> All the products must be returned in the original condition they were   received in along with any bills and labels. Please follow the below   given steps to register your dispute within 7 days of receipt of the   product. Disputes registered after 7 days of delivery may not be   entertained by sellers. Consumable products like food and beverages,   bath and beauty products and bulky items like   furniture/statues/paintings/temples (mandir) cannot be returned. Custom   stitched products cannot be returned.</p>
  </div>
  <div><strong>How to raise dispute for an order?</strong>
    <p>You need to register your dispute in either of the two ways:</p>
    <br>
    <ul>
      <li>1. By sending email to customercare@Kesaron.com with images of   broken/defective/damaged products within 3 days of delivery. We need   photos of the damaged/defective/different products received so that we   can defend your dispute with relevant seller. Please mention your order   number in the email.</li>
      <li>2. Register dispute directly with seller by following below procedure for direct resolution which usually is faster.</li>
      <p>To register dispute directly with seller, please follow below mentioned easy process:</p>
      <br>
      <ul>
        <li>1. Go to your customer account here at <a href="http://www.Kesaron.com/myaccount.php">http://www.Kesaron.com/myaccount.php</a> and sign in when prompted.</li>
        <li>2. Or Click the "My Account link" at the top of the site and sign in when prompted.</li>
        <li>3. Click on the "Confirmed Orders" Tab on the left side and Select the Order you wish to return.</li>
        <li>4. On the order detail page you will see "Raise Dispute with Seller" section.</li>
        <li>5. Enter the details of your dispute and attach relevant images there.</li>
        <li>6. Click "Raise Dispute to Seller" button once ready.</li>
        <li>7. If you are not registered on Kesaron.com, you can register by   clicking the "Register" link on the top of website and then file the   dispute or you can send email to customercare@Kesaron.com</li>
      </ul>
      <p><br>
        After the seller reviews your return request, you will receive an e-mail   with instructions on resolution or return of your item. Please allow   the seller three days to respond to your return request and provide   instructions on returning your item. We recommend using an online   traceable courier service for returning your items.</p>
    </ul>
  </div>
  <div><strong>What to do if Seller is not responding to the dispute?</strong>
    <p>Please allow 3 business days for your seller to address the issue and   provide instructions for your return/ resolution. If the seller does   not respond even after 3 business days, or if your issue is not   addressed to your satisfaction, you can submit the dispute by sending   email to customercare@Kesaron.com and we will ensure you get timely   resolution to your dispute.</p>
  </div>
  <div><strong>Who pays for the delivery charges for returning of products?</strong>
    <p>When you're returning an item that was damaged/ defective or due to   an error on the seller's part, you will be compensated for return   courier charges. You will be paid flat Rs. 100 voucher which can be used   to buy any of the Kesaron products for next 3 months. Please pack   well when you are returning the product so that it reaches the seller in   good condition.</p>
    <br>
    <p>The amount you paid for the product plus any shipping paid at the   time of order will be directly refunded back to your bank account/credit   card/debit card within 15 working days of refund. </p>
  </div>
  <div><strong>When will I be refunded?</strong>
    <p>We can refund you when the seller notifies us of the receipt of the   item back from you or the online tracking shows as delivered to the   seller address. Remember that items have to be returned to sellers   directly by you. Once the seller notifies us of the receipt of the   return item, we will credit the amount back to you within 15 working   days (does not include Saturdays, Sundays and Bank Holidays).</p>
